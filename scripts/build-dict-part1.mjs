function cardFromTuple(t) {
  return {
    phrase: t[0],
    meaning: t[1],
    example: t[2],
    ipaUS: t[3],
    ipaUK: t[4],
    pos: t[5],
  }
}

function extractExistingCards(raw) {
  /** @type {Card[]} */
  const cards = []
  // Match complete card objects
  const re =
    /\{\s*"phrase"\s*:\s*"((?:\\.|[^"\\])*)"\s*,\s*"meaning"\s*:\s*"((?:\\.|[^"\\])*)"\s*,\s*"example"\s*:\s*"((?:\\.|[^"\\])*)"\s*,\s*"ipaUS"\s*:\s*"((?:\\.|[^"\\])*)"\s*,\s*"ipaUK"\s*:\s*"((?:\\.|[^"\\])*)"\s*,\s*"pos"\s*:\s*"((?:\\.|[^"\\])*)"\s*\}/g
  let m
  while ((m = re.exec(raw))) {
    cards.push({
      phrase: m[1],
      meaning: m[2],
      example: m[3],
      ipaUS: m[4],
      ipaUK: m[5],
      pos: m[6],
    })
  }
  return cards
}

function main() {
  const raw = fs.readFileSync(srcPath, 'utf8')
  const existing = extractExistingCards(raw)
  console.log(`Existing complete cards: ${existing.length}`)

  const byKey = new Map()
  for (const c of existing) {
    byKey.set(c.phrase.toLowerCase(), c)
  }

  for (const row of EXTRA) {
    const c = cardFromTuple(row)
    const key = c.phrase.toLowerCase()
    if (!byKey.has(key)) byKey.set(key, c)
  }

  // If still under 300, that is unexpected — pad is not ideal; report
  let cards = [...byKey.values()]
  // Stable alpha order for dictionary browsing consistency
  cards.sort((a, b) => a.phrase.localeCompare(b.phrase, 'en'))

  if (cards.length > 300) {
    cards = cards.slice(0, 300)
  }

  if (cards.length < 300) {
    console.warn(`Only ${cards.length} cards — need more EXTRA entries.`)
  }

  const payload = {
    version: 1,
    deck: { name: 'Từ vựng tiếng Anh A2-C2 · Part 1' },
    meta: {
      part: 1,
      targetCount: 300,
      actualCount: cards.length,
      source: 'Tainguyen/TuDien/Part1.json',
    },
    cards,
  }

  fs.mkdirSync(path.dirname(outWeb), { recursive: true })
  const json = JSON.stringify(payload, null, 2) + '\n'
  fs.writeFileSync(outTainguyen, json)
  fs.writeFileSync(outWeb, json)
  console.log(`Wrote ${cards.length} cards →`)
  console.log(`  ${outTainguyen}`)
  console.log(`  ${outWeb}`)
}

