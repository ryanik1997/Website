function defaultSrs(now: number) {
  return { ease: 2.5, interval: 0, dueAt: now, reps: 0 }
}

export function buildCollocation25Sets(createdAt = Date.now()): TranslationSet[] {
  const sets: TranslationSet[] = []

  for (const genre of Object.keys(DATA) as CollocationGenre[]) {
    const rows = DATA[genre]
    if (rows.length !== 25) {
      throw new Error(`collocation25: ${genre} has ${rows.length} sentences, expected 25`)
    }
    const setId = `tr-collocation-${genre}`
    sets.push({
      id: setId,
      title: TITLES[genre],
      category: 'collocation',
      genre: genre as TranslationGenre,
      cefr: 'B1',
      createdAt,
      sentences: rows.map((row, i) => {
        const [vi, en, hint, difficulty] = row
        const n = String(i + 1).padStart(2, '0')
        return {
          id: `${setId}-s${n}`,
          vi,
          en,
          hint,
          difficulty: difficulty ?? 'medium',
          srsState: defaultSrs(createdAt),
        }
      }),
    })
  }

  return sets
}

